package com.eventmanagement.pojo;

public class booking {
	
	private int id;
	private int uid;
	private String date;
	private String venue;
	private double extra;
	private double meal;
	private double deco;
	private double theme;
	private double total;
	private String request;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getVenue() {
		return venue;
	}
	public void setVenue(String venue) {
		this.venue = venue;
	}
	public double getExtra() {
		return extra;
	}
	public void setExtra(double extra) {
		this.extra = extra;
	}
	public double getMeal() {
		return meal;
	}
	public void setMeal(double meal) {
		this.meal = meal;
	}
	public double getDeco() {
		return deco;
	}
	public void setDeco(double deco) {
		this.deco = deco;
	}
	public double getTheme() {
		return theme;
	}
	public void setTheme(double theme) {
		this.theme = theme;
	}
	public double getTotal() {
		return total;
	}
	public void setTotal(double total) {
		this.total = total;
	}
	public String getRequest() {
		return request;
	}
	public void setRequest(String request) {
		this.request = request;
	}
	
	

}
